import assert from 'node:assert/strict';
import { readFileSync } from 'node:fs';
import {
  FEATURES,
  GRAMMARS,
  generateBatch,
  moreLike,
  familyAdapt,
  tableFit,
  roadPotential,
  svg
} from '../apps/shape-hunt/src/geometry.js';

const index = readFileSync(new URL('../apps/shape-hunt/index.html', import.meta.url), 'utf8');
assert.match(index, /type="module" src="src\/main\.js"/, 'index must load the clean module entrypoint');
assert.ok(!index.includes('app-extensions.js'), 'index must not load legacy layered scripts');
assert.ok(!index.includes('app-stability-guard.js'), 'stability guard should not be needed after refactor');

assert.equal(GRAMMARS.length, 6, 'six evidence grammars expected');
assert.ok(FEATURES.length >= 20, 'feature list should be broad enough for Map Lab');

const shapes = generateBatch({ perGrammar: 20, seed: 42 });
assert.ok(shapes.length >= GRAMMARS.length * 20, 'batch should include generated shapes');
assert.ok(shapes.every(shape => shape.generatorVersion === 'shape-hunt-core-v1'), 'all shapes must be generated by the clean core');
assert.ok(shapes.every(shape => Number.isFinite(shape.verifiedEvidenceScore)), 'evidence score must be numeric');
assert.ok(shapes.every(shape => shape.smoothCurvatureScore >= 0 && shape.smoothCurvatureScore <= 1), 'curvature score range');

const sample = shapes[0];
for (const size of [120, 240, 330]) {
  const adapted = size === 330 ? sample : familyAdapt(sample, size);
  assert.ok(adapted.height > 30, `height for ${size}`);
  assert.ok(adapted.base > 30, `base for ${size}`);
  assert.ok(adapted.rim > 40, `rim for ${size}`);
  assert.match(svg(sample, { familySize: size }), /<svg/, `svg for ${size}`);
}

assert.ok(Number.isFinite(tableFit(sample).score), 'table fit score');
assert.ok(Number.isFinite(roadPotential(sample).score), 'road score');

const variants = moreLike(sample, { count: 30, seed: 99 });
assert.equal(variants.length, 30, 'moreLike count');
assert.ok(variants.every(shape => shape.archetypeId === sample.archetypeId), 'moreLike should keep grammar');
assert.ok(variants.every(shape => shape.verifiedEvidenceScore >= 0), 'variants scored');

console.log('Shape Hunt validation passed.');
